let dia = prompt("Dia da semana (0-6):");

if(dia == 0){
    console.log("Domingo");
}else if(dia == 1){
    console.log("Segunda");
}else if(dia == 2){
    console.log("Terça");
}else if(dia == 3){
    console.log("Quarta");
}else if(dia == 4){
    console.log("Quinta");
}else if(dia == 5){
    console.log("Sexta");
}else if(dia == 6){
    console.log("Sábado");
}else{
    console.log('Não é um dia da semana válido!!!');
}

console.log("fim de código");





